//------ Menu Responsive------//
//con el classList se llama al estilo css open, y con el add se agrega
function mostrar() {
    document.getElementById("sliding_menu").classList.add('open'); 
  }     
//con el classList se llama al estilo css open, y con el remve se remueve
function ocultar() {
    document.getElementById("sliding_menu").classList.remove('open'); 
  } 
//------Fin Menu Responsive------//